namespace RenderDemo
{
	/// <summary>
	/// The default action when additional user input is required.
	/// </summary>
	public enum OverwriteAction
	{
		/// <summary>
		/// Ask the user.
		/// </summary>
		Ask = 0,
		/// <summary>
		/// Automatically answer yes.
		/// </summary>
		AutoYes = 1,
		/// <summary>
		/// Automatically answer no.
		/// </summary>
		AutoNo = 2
	}
}
